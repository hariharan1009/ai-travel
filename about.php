 document.addEventListener("DOMContentLoaded", function() {
  const counters = document.querySelectorAll('.counter');
  const keyParameters = document.querySelector('.key-parameters'); // The section to hover over
  
  function animateCounters() {
    counters.forEach(counter => {
      const target = +counter.getAttribute('data-count');
      let count = 0;
      
      const interval = setInterval(() => {
        count += Math.ceil(target / 50); // Speed up the animation by increasing the increment
        counter.textContent = count + "+"; // Add "+" to the count
        
        if (count >= target) {
          clearInterval(interval);
          counter.textContent = target + "+"; // Ensure it reaches the target value with "+"
        }
      }, 30); // Adjust the interval to make the animation faster (lower value = faster)
    });
  }

  // Start animation on hover
  keyParameters.addEventListener('mouseenter', function() {
    animateCounters(); // Start animation when hovering over the section
  });
});

</script>
